    public int drawTree(Graphics g, NArbol x, int x0, int x1, int y){
        int m = (x0 + x1) / 2;
        g.setColor(Color.BLUE);
        g.fillOval(m, y, 50, 40);
        g.setColor(Color.lightGray);
        g.setFont(new Font("Arial",Font.BOLD,20));
        String t = String.valueOf(x.getDato());
        g.drawString(t, m+20, y+30);
        if (x.getIzq() != null) {
            int x2 = drawTree(g, x.getIzq(),x0,m,y+50);
            g.drawLine(m+25, y+40, x2+25, y+50);
        }
        if (x.getDer() != null) {
            int x2 = drawTree(g, x.getDer(),m,x1,y+50);
            g.drawLine(m+25, y+40, x2+25, y + 50);
        }
        return m;
    }
